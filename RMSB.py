def findBound(graph, R, k):
    nodeBound = {}
    for node in graph.nodes:
        profit = 0
        results = []
        check = set()
        check.add(node)
        currentLevel = set()
        currentLevel.add(node)
        results.append(currentLevel)
        for i in range(k):
            next = set()
            for nodd in currentLevel:
                for neig in graph.get_neighbor(nodd):
                    if neig not in check:
                        next.add(neig)
                        check.add(neig)
            currentLevel = next
            results.append(currentLevel)
        for i in range(k):
            profit += R[i] * len(results[i])
        bound = profit / (R[0] - R[1])
        nodeBound[node] = bound
    max = 0
    for node in graph.nodes:
        if nodeBound[node] > max:
            max = nodeBound[node]
    return max

